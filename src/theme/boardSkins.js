export function getBoardSkinVars(skinId, theme = 'light') {
  const skin = boardSkinVars[skinId] ?? boardSkinVars.classic;
  return skin[theme] ?? skin.light;
}

export function isPremiumSkin(skinId) {
  return boardSkins.some((skin) => skin.id === skinId && skin.tier === 'pro');
}

export function isKnownSkin(skinId) {
  return boardSkins.some((skin) => skin.id === skinId);
}
